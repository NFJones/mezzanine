/// Carries Socket Selection state for this subsystem.
///
/// The type keeps related data explicit so callers can inspect and move
/// structured runtime state without parsing display text.
#[derive(Debug, Clone, PartialEq, Eq)]
pub(super) enum SocketSelection {
    /// Represents the Default case for this enumeration.
    ///
    /// Callers use this variant to describe one explicit state or command path
    /// without relying on stringly typed status values.
    Default(PathBuf),
    /// Represents the Explicit case for this enumeration.
    ///
    /// Callers use this variant to describe one explicit state or command path
    /// without relying on stringly typed status values.
    Explicit(PathBuf),
    /// Represents the Named case for this enumeration.
    ///
    /// Callers use this variant to describe one explicit state or command path
    /// without relying on stringly typed status values.
    Named(PathBuf),
    /// Represents the In Pane case for this enumeration.
    ///
    /// Callers use this variant to describe one explicit state or command path
    /// without relying on stringly typed status values.
    InPane(PathBuf),
}

/// Top-level process CLI commands accepted by `mez`.
///
/// This enum is the process-argv command surface. Interactive shell commands
/// and agent slash commands intentionally use separate parsers.
#[derive(Debug, Clone, Subcommand)]
pub(super) enum CliCommand {
    /// Manages Mezzanine configuration.
    Config(super::config::ConfigCliArgs),
    /// Starts a background session daemon and attaches when interactive.
    #[command(visible_alias = "new-session")]
    New(super::serve::NewCliArgs),
    /// Starts a foreground control daemon for a new session.
    #[command(visible_alias = "daemon")]
    Serve(super::serve::ServeCliArgs),
    /// Lists resumable sessions known to this client.
    #[command(visible_alias = "list-sessions")]
    List,
    /// Reattaches the primary terminal or requests observer access.
    #[command(visible_alias = "attach-session")]
    Attach(super::attach::AttachCliArgs),
    /// Detaches the current or specified control client.
    #[command(visible_alias = "detach-client")]
    Detach(DetachCliArgs),
    /// Terminates a live session through the control socket.
    KillSession(KillSessionCliArgs),
    /// Manages persisted snapshots.
    Snapshot(super::snapshot::SnapshotCliArgs),
    /// Manages provider authentication.
    Auth(super::auth::AuthCliArgs),
    /// Manages MCP server configuration.
    Mcp(super::mcp::McpCliArgs),
    /// Manages local project issues.
    Issue(super::issue::IssueCliArgs),
    /// Manages persistent agent memory.
    Memory(super::memory::MemoryCliArgs),
    /// Prints version information.
    Version,
}

