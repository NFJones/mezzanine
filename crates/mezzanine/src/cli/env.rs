impl CliInvocation {
    /// Runs the parse operation for this subsystem.
    ///
    /// The function keeps parsing, state changes, and error propagation in
    /// the owning module so callers receive typed results instead of relying
    /// on duplicated control-flow logic.
    #[cfg(test)]
    pub(super) fn parse(
        args: &[String],
        runtime: &RuntimeEnv,
        mez: Option<&OsString>,
    ) -> Result<Self> {
        match Self::parse_or_display(args, runtime, mez)? {
            CliInvocationParse::Invocation(invocation) => Ok(*invocation),
            CliInvocationParse::Display(display) => Err(MezError::invalid_args(display)),
        }
    }

    /// Parses the process argv and preserves clap-rendered help/version
    /// responses as a successful display outcome.
    ///
    /// # Parameters
    /// - `args`: The process argv beginning with the executable name.
    /// - `runtime`: Runtime environment used for default socket selection.
    /// - `mez`: Optional in-pane environment value.
    pub(super) fn parse_or_display(
        args: &[String],
        runtime: &RuntimeEnv,
        mez: Option<&OsString>,
    ) -> Result<CliInvocationParse> {
        let mut socket_selection = None;
        let parsed = match CliArgv::try_parse_from(args) {
            Ok(parsed) => parsed,
            Err(error)
                if matches!(
                    error.kind(),
                    clap::error::ErrorKind::DisplayHelp | clap::error::ErrorKind::DisplayVersion
                ) =>
            {
                return Ok(CliInvocationParse::Display(error.to_string()));
            }
            Err(error) => return Err(MezError::invalid_args(error.to_string())),
        };

        if let Some(socket_path) = parsed.socket {
            if !socket_path.is_absolute() {
                return Err(MezError::invalid_args(
                    "-S requires an absolute socket path",
                ));
            }
            socket_selection = Some(set_socket_selection(
                socket_selection,
                SocketSelection::Explicit(socket_path),
            )?);
        }
        if let Some(name) = parsed.socket_name {
            let directory = default_socket_directory(runtime)?;
            let socket_path = socket_path_for_name(&directory.path, &name)?;
            socket_selection = Some(set_socket_selection(
                socket_selection,
                SocketSelection::Named(socket_path),
            )?);
        }

        let socket_selection = match socket_selection {
            Some(selection) => selection,
            None => match socket_selection_from_mez(mez)? {
                Some(selection) => selection,
                None => default_socket_selection(runtime)?,
            },
        };

        Ok(CliInvocationParse::Invocation(Box::new(Self {
            command: parsed.command,
            socket_selection,
            output_format: CliOutputFormat::from_json_flag(parsed.json),
        })))
    }
}

/// Renders clap help for one process CLI command path.
///
/// # Parameters
/// - `path`: Command names from the top-level command down to the target.
pub(super) fn render_cli_help(path: &[&str]) -> Result<String> {
    let argv = std::iter::once("mez".to_string())
        .chain(path.iter().map(|part| (*part).to_string()))
        .chain(std::iter::once("--help".to_string()));
    match CliArgv::try_parse_from(argv) {
        Err(error) if matches!(error.kind(), clap::error::ErrorKind::DisplayHelp) => {
            Ok(error.to_string())
        }
        Err(error) => Err(MezError::invalid_args(error.to_string())),
        Ok(_) => Err(MezError::invalid_state(
            "clap did not render help for a requested command path",
        )),
    }
}

/// Renders clap version output for the process CLI.
pub(super) fn render_cli_version() -> Result<String> {
    match CliArgv::try_parse_from(["mez", "--version"]) {
        Err(error) if matches!(error.kind(), clap::error::ErrorKind::DisplayVersion) => {
            Ok(error.to_string())
        }
        Err(error) => Err(MezError::invalid_args(error.to_string())),
        Ok(_) => Err(MezError::invalid_state(
            "clap did not render version for the process CLI",
        )),
    }
}

/// Runs the default socket selection operation for this subsystem.
///
/// The function keeps parsing, state changes, and error propagation in
/// the owning module so callers receive typed results instead of relying
/// on duplicated control-flow logic.
pub(super) fn default_socket_selection(runtime: &RuntimeEnv) -> Result<SocketSelection> {
    let directory = default_socket_directory(runtime)?;
    Ok(SocketSelection::Default(socket_path_for_name(
        &directory.path,
        DEFAULT_SOCKET_NAME,
    )?))
}

/// Runs the socket selection from mez operation for this subsystem.
///
/// The function keeps parsing, state changes, and error propagation in
/// the owning module so callers receive typed results instead of relying
/// on duplicated control-flow logic.
pub(super) fn socket_selection_from_mez(
    value: Option<&OsString>,
) -> Result<Option<SocketSelection>> {
    let Some(value) = value else {
        return Ok(None);
    };
    let value = value.to_string_lossy();
    if value.trim().is_empty() {
        return Ok(None);
    }
    let socket = value
        .split(MEZ_ENV_FIELD_SEPARATOR)
        .next()
        .unwrap_or_default()
        .trim();
    if socket.is_empty() {
        return Ok(None);
    }
    let path = PathBuf::from(socket);
    if !path.is_absolute() {
        return Err(MezError::invalid_args(
            "MEZ contains a relative socket path",
        ));
    }
    Ok(Some(SocketSelection::InPane(path)))
}

/// Runs the set socket selection operation for this subsystem.
///
/// The function keeps parsing, state changes, and error propagation in
/// the owning module so callers receive typed results instead of relying
/// on duplicated control-flow logic.
pub(super) fn set_socket_selection(
    existing: Option<SocketSelection>,
    next: SocketSelection,
) -> Result<SocketSelection> {
    if existing.is_some() {
        Err(MezError::invalid_args(
            "only one control socket selector may be provided",
        ))
    } else {
        Ok(next)
    }
}
/// Runs the terminal size from environment operation for this subsystem.
///
/// The function keeps parsing, state changes, and error propagation in
/// the owning module so callers receive typed results instead of relying
/// on duplicated control-flow logic.
pub(super) fn terminal_size_from_environment() -> (u16, u16) {
    let columns = std::env::var("COLUMNS")
        .ok()
        .and_then(|value| value.parse::<u16>().ok())
        .filter(|value| *value > 0)
        .unwrap_or(80);
    let rows = std::env::var("LINES")
        .ok()
        .and_then(|value| value.parse::<u16>().ok())
        .filter(|value| *value > 0)
        .unwrap_or(24);
    (columns, rows)
}

/// Returns the live terminal size from a TTY file descriptor when available,
/// falling back to shell-provided `COLUMNS`/`LINES` and finally 80x24.
///
/// Default `mez` launch starts a background daemon before attaching the primary
/// client, so the daemon cannot query the parent terminal itself. The attach
/// side must prefer the real TTY size for `control/initialize` so the initial
/// pane is resized before its first user-facing render.
pub(super) fn terminal_size_from_fd_or_environment(fd: Option<RawFd>) -> (u16, u16) {
    if let Some(fd) = fd
        && let Ok(Some(size)) = read_attached_terminal_size(fd)
    {
        return (size.columns, size.rows);
    }
    terminal_size_from_environment()
}

/// Runs the selected socket path operation for this subsystem.
