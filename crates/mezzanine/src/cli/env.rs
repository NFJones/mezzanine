/// Represents a process argv parse result that either produced an invocation
/// or a clap-rendered informational response.
#[derive(Debug, Clone)]
pub(super) enum CliInvocationParse {
    /// A runnable command invocation.
    Invocation(Box<CliInvocation>),
    /// A help or version response generated by clap.
    Display(String),
}

#[derive(Debug, Parser)]
